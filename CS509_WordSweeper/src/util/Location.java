package util;

public class Location {
	int column;
	int row;
	
	
	public Location(int column, int row) {
		super();
		this.column = column;
		this.row = row;
	}


	public int getColumn() {
		return column;
	}


	public int getRow() {
		return row;
	}


	public void setColumn(int column) {
		this.column = column;
	}


	public void setRow(int row) {
		this.row = row;
	}
	
	
	
	

}
